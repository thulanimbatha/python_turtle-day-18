from turtle import Turtle, Screen

# turtle object
tmnt = Turtle()
tmnt.shape("turtle")
tmnt.color("turquoise") # change colour
tmnt.forward(200)   # move Turtle 200 paces
tmnt.right(90)  # turn turtle right 90 degrees
tmnt.forward(200)
tmnt.right(90)
tmnt.forward(200)
tmnt.right(45)
tmnt.forward(200)
tmnt.right(90)





